let my ={
    name : 'Rohini',
    gender : "Female",
    age : '35'
}
// dot notation to access object key values
console.log(my.name)
console.log(my.gender)
console.log(my.age)

//bracket notation
console.log(my['name'])
console.log(my['gender'])
console.log(my['age'])

// adding new property to an object
//dot notation
my.email = 'rohinikr2004@gmail.com'
console.log(my)
//bracket notation
my['address'] = 'Aylesbury'
console.log(my)

