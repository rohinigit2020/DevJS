// programme to print addition, substraction, multiplication, division 
// and module(remainder) of two numbers

let a = 20;
let b = 10;

//addition
console.log('Addition of two numbers',a,'and',b, 'is',a + b)

//Substraction
console.log('Substraction of two numbers',a,'and',b, 'is',a - b)

//Multiplication
console.log('Multiplication of two numbers',a,'and',b, 'is',a * b)

//Division
console.log('Division of two numbers',a,'and',b, 'is',a / b)

//Module
console.log('Module of two numbers',a,'and',b, 'is',a % b)
